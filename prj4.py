def Rectangule(x,y,a,b,color,angel):
    t.pendown ()
    t.color (color)
    t.penup ()
    t.setx ( x )
    t.sety ( y )
    t.begin_fill ()
    t.pendown ( )
    t.lt(angel)
    t.forward (a)
    t.lt(angel)
    t.forward (b)
    t.lt (angel)
    t.forward (a)
    t.goto(x, y)
    t.end_fill ()

def Circ(x,y,r,color):
    t.pendown ()
    t.color (color)
    t.penup ()
    t.setx ( x )
    t.sety ( y )
    t.begin_fill ()
    t.pendown ( )
    t.circle(r)
    t.end_fill ()

