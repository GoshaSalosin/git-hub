import turtle
#рисунки
t = turtle.Pen()
def Square(x,y,speed,size,color,angel):
    t.pendown ()
    t.color(color)
    t.penup()
    t.setx(x)
    t.sety(y)
    t.speed(speed)
    t.begin_fill()
    t.pendown()
    t.forward(size)
    t.right(angel)
    t.forward(size)
    t.right(angel)
    t.forward(size)
    t.right(angel)
    t.forward(size)
    t.end_fill()

def Triangle(x,y,a,h,color,angel):
    t.penup()
    t.setx(x)
    t.sety(y)
    t.color(color)
    t.begin_fill()
    t.pendown()
    t.rt(angel)
    t.forward(a)
    t.lt(angel)
    t.forward(h)
    t.goto(x,y)
    t.end_fill()

